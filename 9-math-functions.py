import math

# search for python 3 math module to view all the math functions available

# after importing the math library, you are able to do complex math functions
# takes in radian value and results in a float
print(math.sin(2))
# shows 0.9092974268256817

# to get ceiling of a number
print(math.ceil(2.9))
# shows 3 in terminal

# to get the floor value of a number
print(math.floor(2.9))
# shows 2 

# to round numbers
x=2.9
print(round(x))

# to get the absolute value of a number
# in math example: |-3.3| = 3.3
print(abs(-2.9))
# shows 2.9