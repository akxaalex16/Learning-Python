x = 10 + 3 * 2
print(x)
# shows 16
# 3 multiplied by 2 first, then add 10

# order:
# parenthesis ()
# exponents **
# multiplication * or division / //
# addition + or subtraction -

y = 10 + 3 * 2 ** 2
print(y)
# shows 22
# first 2 to the power of 2 which is 4, 
# then 4 multiplied by 3 which is 12,
# then 12 + 10 = 22

z = (10 + 3) * 2 ** 2
print(z)
# shows 52
# first 10 plus 3 which is 13,
# then 2 to the power of 2 which is 4,
# then 13 multiplied by 4 = 52

# another example 
a = (2 + 3) * 10 - 3
print(a)
# shows 47
# 2 plus 3 = 5
# 5 times 10 = 50
# 50 minus 3 = 47 