# addition
print(10 + 6)

# subtraction
print(20 - 4)

# multiplication
print(4 * 4)

# division
# one / means we get a float value in return 
print(16 / 1)
# two // means we get an integer value in return 
print(16 // 1)

# modulus operator %
# returns the remainder of the division
# shows 1
print(16 % 3)

# exponents or power **
# this example reads 2 to the power of 9
# shows 512
print(2 ** 9)

# to increment a value
x = 10
x = x + 6
print(x)

# augmented assignment operator
y = 10
y += 6
print(y)
z = 20
z -= 4
print(z)